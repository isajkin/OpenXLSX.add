{
	int i = 0;
	if (!name || !*name)return -1;
	while (1) {
		if (!*name_rgb[i].name)break;
		if (!_stricmp(name, name_rgb[i].name))return name_rgb[i].rgb;
		i++;

	}
	return -1;
}
